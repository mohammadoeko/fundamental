package com.juaracoding.daytwo;

public class Variable {
    public static void main(String[] args) {
        //Numeric
        int usia = 20, operasi = 10*5;
        float nilai = 8.7f;
        double nilaiProgramming = 8.9;

        System.out.println(usia);
        System.out.println(nilai);
        System.out.println(nilaiProgramming);
        System.out.println(operasi);

        //Char, Text
        char huruf = 'A';
        System.out.println(huruf);

        String alamat = "Kuningan";
        System.out.println(alamat);

        //Boolean
        boolean isAddress = true;
        System.out.println(isAddress);
    }
}
