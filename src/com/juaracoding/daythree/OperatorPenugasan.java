package com.juaracoding.daythree;

public class OperatorPenugasan {
    public static void main(String[] args) {
        int a, b, c, d, e;
        a = 5;
        b= 3;
        b = b + 1;
        System.out.println(b);
        System.out.println(b += 1); // artinya b = b + 1
        System.out.println(b %= 2); // artinya b = b % 2
        System.out.println(10 << 3); // 10 * 2 ^ 3
        System.out.println(49 >> 3); // 40 / 2 ^ 3
    }
}
